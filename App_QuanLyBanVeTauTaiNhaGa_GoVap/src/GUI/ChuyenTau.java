    private void fillTableWithNewLichTrinh(String ma) {
    DAO_ChuyenTau lichTrinhDAO = new DAO_ChuyenTau();
      LichTrinhTau lichTrinh = lichTrinhDAO.getTheoMa(ma);

    // Nếu không tìm thấy lịch trình
    if (lichTrinh == null) {
        JOptionPane.showMessageDialog(this, "Không tìm thấy lịch trình với mã: " + ma, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        return;
    }

    // Điền dữ liệu vào bảng
    DefaultTableModel model = (DefaultTableModel) jTable_LichTrinh.getModel();
    model.setRowCount(0); // Xóa tất cả các dòng hiện tại

     Object[] rowData = new Object[6]; // Giả sử có 6 cột
        
        rowData[0] = lichTrinh.getMaLichTrinh();
        rowData[1] = lichTrinh.getTau().getTenTau(); // Nếu bạn có tên tàu
        rowData[2] = lichTrinh.getGioDi();
        rowData[3] = lichTrinh.getNgayDi();
        rowData[4] = lichTrinh.getTau().getTuyenTau().getTenTuyen(); // Lấy tên tuyến từ đối tượng TuyenTau
        rowData[5] = lichTrinh.getTrangThai();
    
    model.addRow(rowData);
}
