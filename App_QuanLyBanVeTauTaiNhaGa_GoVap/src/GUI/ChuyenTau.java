    public void fillComboBoxTau() {
        DAO_ChuyenTau dao = new DAO_ChuyenTau();
        List<Tau> listTau = dao.getAllTau(); // Gọi phương thức lấy dữ liệu tàu

        jComboBoxTau.removeAllItems(); // Xóa các mục hiện tại trong ComboBox

        // Sử dụng Set để theo dõi các tên tàu đã thêm
        Set<String> uniqueTauNames = new HashSet<>();

        for (Tau tau : listTau) {
            String tenTau = tau.getTenTau();
            // Kiểm tra nếu tên tàu chưa được thêm vào Set
            if (uniqueTauNames.add(tenTau)) {
                jComboBoxTau.addItem(tenTau);
                cbb_Tau.addItem(tenTau);// Thêm tên tàu vào ComboBox nếu chưa tồn tại
            }
        }
    }
