    private void btnTimVeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimVeActionPerformed
        // TODO add your handling code here:
               // Lấy giá trị từ txtNhapMa
        String maNhap = txtNhapMa.getText().trim(); // Lấy và làm sạch dữ liệu nhập vào

        // Kiểm tra nếu txtNhapMa không rỗng
        if (maNhap.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập thông tin tìm kiếm.");
            return; // Dừng nếu không có giá trị
        }

        // Kiểm tra radio nào được chọn và thực hiện hành động tương ứng
        if (rdCCCD.isSelected()) {
            // Thực hiện tìm kiếm theo CCCD
           hoaDons = dao_traCuuHD.timHoaDonTheoCCCD(maNhap);
           if(hoaDons== null){
                JOptionPane.showMessageDialog(null, "Không tìm thấy hóa đơn với cccd");
           }
        } else if (rdHDHuyDoi.isSelected()) {
            // Thực hiện tìm kiếm theo mã hóa đơn hủy đổi
            hoaDons =dao_traCuuHD.timHoaDonTheoMaLHD("LHD03");
             if(hoaDons== null){
                JOptionPane.showMessageDialog(null, "Không tìm thấy hóa đơn hủy");
           }
        } else if (rdMaHD.isSelected()) {
            // Thực hiện tìm kiếm theo mã hóa đơn
           hoaDons = (List<HoaDon>) dao_traCuuHD.timHoaDonTheoMaHD(maNhap);
            if(hoaDons== null){
                JOptionPane.showMessageDialog(null, "Không tìm thấy hóa đơn với mã hóa đơn");
           }
        } else if (rdMaKM.isSelected()) {
            // Thực hiện tìm kiếm theo mã khuyến mãi
            hoaDons = dao_traCuuHD.timHoaDonTheoMaKM(maNhap);
             if(hoaDons== null){
                JOptionPane.showMessageDialog(null, "Không tìm thấy hóa đơn với mã khuyến mãi");
           }
        } else {
            // Nếu không có radio nào được chọn
            JOptionPane.showMessageDialog(null, "Vui lòng chọn một tiêu chí tìm kiếm.");
        }
          themThongTinVaoTable(hoaDons);
    }//GEN-LAST:event_btnTimVeActionPerformed

