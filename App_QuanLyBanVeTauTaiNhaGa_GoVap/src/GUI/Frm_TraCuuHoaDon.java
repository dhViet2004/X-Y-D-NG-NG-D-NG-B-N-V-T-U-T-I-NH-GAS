    private void btnLocHoaDonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLocHoaDonActionPerformed
        // Lấy ngày bắt đầu và ngày kết thúc từ các bộ chọn ngày
    Date ngayBatDau = jDateChooseLocNBD.getDate();
    Date ngayKetThuc = jDateChooserLocNKT.getDate();

    // Kiểm tra nếu ngày bắt đầu lớn hơn ngày kết thúc
    if (ngayBatDau != null && ngayKetThuc != null) {
        // Chuyển đổi Date thành LocalDate
        LocalDate startDate = ngayBatDau.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        LocalDate endDate = ngayKetThuc.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();

        // Kiểm tra nếu ngày bắt đầu sau ngày kết thúc
        if (startDate.isAfter(endDate)) {
            JOptionPane.showMessageDialog(null, "Ngày bắt đầu không được sau ngày kết thúc.");
            return;  // Dừng hàm nếu ngày không hợp lệ
        }

        // Lấy giá trị từ các JTextField txtMaxCost và txtMinCost
        String minCostText = txtMinCost.getText();
        String maxCostText = txtMaxCost.getText();
        
        double minCost = 0.0;
        double maxCost = Double.MAX_VALUE;

        // Kiểm tra nếu người dùng nhập giá trị tối thiểu và tối đa
        try {
            if (!minCostText.isEmpty()) {
                minCost = Double.parseDouble(minCostText);  // Chuyển đổi giá trị nhập thành double
            }
            if (!maxCostText.isEmpty()) {
                maxCost = Double.parseDouble(maxCostText);  // Chuyển đổi giá trị nhập thành double
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập giá trị hợp lệ cho mức giá.");
            return;  // Dừng nếu giá trị không hợp lệ
        }

        // Gọi hàm lọc để lấy danh sách hóa đơn trong khoảng thời gian và theo mức giá
        List<HoaDon> hoaDons = dao_traCuuHD.timHoaDonTheoKhoangThoiGian(startDate, endDate, minCost, maxCost);
        
        // Thêm thông tin vào bảng
        themThongTinVaoTable(hoaDons);
    } else {
        // Thông báo nếu người dùng chưa chọn cả ngày bắt đầu và ngày kết thúc
        JOptionPane.showMessageDialog(null, "Vui lòng chọn cả ngày bắt đầu và ngày kết thúc.");
    }
    }//GEN-LAST:event_btnLocHoaDonActionPerformed

