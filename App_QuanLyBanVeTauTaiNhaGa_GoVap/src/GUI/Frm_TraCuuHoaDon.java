    private void themThongTinVaoTable(List<HoaDon> hoaDons) {
                // Xóa tất cả các dòng hiện tại trong bảng trước khi thêm dữ liệu mới
             tableModel.setRowCount(0);
             // Duyệt qua danh sách các hóa đơn và thêm vào tableModel
         for (HoaDon hoaDon : hoaDons) {
             String tenLoai;

             // Kiểm tra điều kiện và xác định giá trị của tenLoai
             if (hoaDon.getLoaiHoaDon().getMaLoaiHoaDon().equals("LHD01")) {
                 tenLoai = "Thanh toán";
             } else {
                 tenLoai = "Trả Vé";
             }

             // Tạo mảng rowData với các giá trị từ hoaDon
             Object[] rowData = new Object[] {
                 hoaDon.getMaHD(),
                 hoaDon.getKhachHang().getTenKhachHang(),
                 hoaDon.getKhuyenMai().getMaKM(),
                 tenLoai,  // Đưa giá trị tenLoai đã xác định vào đây
                 hoaDon.getNgayLap(),
                 hoaDon.getTienGiam(),
                 hoaDon.getTongTien()
             };

             // Thêm dòng vào tableModel
             tableModel.addRow(rowData);
         }

    }
